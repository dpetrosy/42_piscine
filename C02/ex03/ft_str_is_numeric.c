int	ft_str_is_numeric(char *str)
{
	int		k;
	char	c;

	if (*str == '\0')
		return (1);
	c = *str;
	while (c != '\0')
	{
		if (c >= 48 && c <= 57)
			k = 1;
		else
			return (0);
		str = str + 1;
		c = *str;
	}
	return (1);
}
