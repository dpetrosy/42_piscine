char	*ft_strcat(char *dest, char *src)
{
	int		i;
	int		j;
	char	*ret_dest;

	ret_dest = dest;
	i = 0;
	j = 0;
	while (dest[i] != '\0')
		++i;
	while (src[j] != '\0')
	{
		dest[i] = src[j];
		++i;
		++j;
	}
	dest[i] = '\0';
	return (ret_dest);
}
